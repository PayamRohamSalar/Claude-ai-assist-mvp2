    def _setup_dependencies(self) -> bool:
        """Install and verify required dependencies"""
        
        try:
            # Check pip availability
            result = subprocess.run([sys.executable, '-m', 'pip', '--version'], 
                                 capture_output=True, text=True)
            
            if result.returncode != 0:
                print("  ‚ùå pip not available")
                return False
            
            print(f"  ‚úÖ pip available")
            
            # Check each required package
            missing_packages = []
            installed_packages = []
            
            for package in self.required_packages:
                try:
                    __import__(package.replace('-', '_'))
                    installed_packages.append(package)
                    print(f"  ‚úÖ {package}")
                except ImportError:
                    missing_packages.append(package)
                    print(f"  ‚ùå {package} (missing)")
            
            # Install missing packages
            if missing_packages:
                print(f"\n  üì¶ ŸÜÿµÿ® {len(missing_packages)} Ÿæ⁄©€åÿ¨ ŸÖŸÅŸÇŸàÿØ...")
                
                for package in missing_packages:
                    print(f"    üîÑ ŸÜÿµÿ® {package}...")
                    result = subprocess.run([
                        sys.executable, '-m', 'pip', 'install', package
                    ], capture_output=True, text=True)
                    
                    if result.returncode == 0:
                        print(f"    ‚úÖ {package} ŸÜÿµÿ® ÿ¥ÿØ")
                    else:
                        print(f"    ‚ùå ÿÆÿ∑ÿß ÿØÿ± ŸÜÿµÿ® {package}")
                        self.logger.error(f"Failed to install {package}: {result.stderr}")
            
            # Final verification
            all_installed = True
            for package in self.required_packages:
                try:
                    __import__(package.replace('-', '_'))
                except ImportError:
                    all_installed = False
                    break
            
            if all_installed:
                print(f"  üéâ ÿ™ŸÖÿßŸÖ {len(self.required_packages)} Ÿæ⁄©€åÿ¨ ÿ¢ŸÖÿßÿØŸá ÿßÿ≥ÿ™")
                return True
            else:
                print(f"  ‚ùå ÿ®ÿ±ÿÆ€å Ÿæ⁄©€åÿ¨‚ÄåŸáÿß ŸáŸÜŸàÿ≤ ŸÖŸÅŸÇŸàÿØ Ÿáÿ≥ÿ™ŸÜÿØ")
                return False
                
        except Exception as e:
            self.logger.error(f"Error setting up dependencies: {str(e)}")
            return False
    
