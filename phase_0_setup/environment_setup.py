    def _check_ollama_setup(self) -> bool:
        """Check Ollama installation and models"""
        
        try:
            # Check if Ollama is available
            result = subprocess.run(['ollama', '--version'], capture_output=True, text=True)
            
            if result.returncode != 0:
                print("  ‚ùå Ollama not found")
                print("  üí° ŸÜÿµÿ® Ollama: https://ollama.ai/download")
                return False
            
            ollama_version = result.stdout.strip()
            print(f"  ‚úÖ {ollama_version}")
            
            # Check if Ollama service is running
            result = subprocess.run(['ollama', 'list'], capture_output=True, text=True)
            
            if result.returncode != 0:
                print("  ‚ùå ÿ≥ÿ±Ÿà€åÿ≥ Ollama ÿØÿ± ÿ≠ÿßŸÑ ÿßÿ¨ÿ±ÿß ŸÜ€åÿ≥ÿ™")
                print("  üí° ÿ¥ÿ±Ÿàÿπ ÿ≥ÿ±Ÿà€åÿ≥: ollama serve")
                return False
            
            # Parse available models
            models_output = result.stdout.strip()
            if not models_output or "NAME" not in models_output:
                print("  ‚ö†Ô∏è Ÿá€å⁄Ü ŸÖÿØŸÑ€å ŸÜÿµÿ® ŸÜÿ¥ÿØŸá")
                return False
            
            # Extract model names
            lines = models_output.split('\n')[1:]  # Skip header
            available_models = []
            
            for line in lines:
                if line.strip():
                    model_name = line.split()[0]
                    available_models.append(model_name)
                    print(f"  üìñ {model_name}")
            
            # Check if required models are available
            config = get_config_manager().get_config()
            required_model = config.llm.ollama_model
            backup_model = config.llm.ollama_backup_model
            
            model_check = False
            if any(required_model in model for model in available_models):
                print(f"  ‚úÖ ŸÖÿØŸÑ ÿßÿµŸÑ€å ŸÖŸàÿ¨ŸàÿØ: {required_model}")
                model_check = True
            elif any(backup_model in model for model in available_models):
                print(f"  ‚úÖ ŸÖÿØŸÑ Ÿæÿ¥ÿ™€åÿ®ÿßŸÜ ŸÖŸàÿ¨ŸàÿØ: {backup_model}")
                model_check = True
            else:
                print(f"  ‚ö†Ô∏è ŸÖÿØŸÑ‚ÄåŸáÿß€å ŸÖŸàÿ±ÿØ ŸÜ€åÿßÿ≤ ŸÖŸàÿ¨ŸàÿØ ŸÜ€åÿ≥ÿ™")
                print(f"  üí° ŸÜÿµÿ® ŸÖÿØŸÑ: ollama pull {required_model}")
            
            return model_check
            
        except FileNotFoundError:
            print("  ‚ùå Ollama not found in PATH")
            return False
        except Exception as e:
            self.logger.error(f"Error checking Ollama: {str(e)}")
            return False
    
